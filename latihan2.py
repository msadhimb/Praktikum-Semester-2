#Luas Segitiga

alas = int(input("Alas Segitiga : "))
tinggi = int(input("Tinggi Segitiga : "))

luas = alas * tinggi
print("Luas Segitiga adalah ", luas)

#keliling Segitiga

sisi1 = int(input("Sisi 1 Segitiga : "))
sisi2 = int(input("Sisi 2 Segitiga : "))
sisi3 = int(input("Sisi 3 Segitiga : "))

keliling = sisi1 + sisi2 + sisi3
print("Keliling Segitiga adalah ", keliling)

